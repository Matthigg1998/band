class Person:
    """Represents a person.

    Attributes:
        name (str): The person's name.
        age (int): The person's age.
    """

    def __init__(self, name, age):
        """
        Initialize a new Person object.

        Args:
            name (str): The person's name.
            age (int): The person's age.
        """
        self.name = name
        self.age = age
